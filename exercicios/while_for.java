
public class while_for {

	public static void main(String[] args) {

		//Algoritmo que exibe os números de 1 até 100
		
		//inicialização
		int num = 1;
		
		//condição
		while(num <= 100){
			
			System.out.println(num);
			
			//incremento
			num++;
		}
		
		
		
		for(num = 1;  num <= 100 ; num++)
			System.out.println(num);
		

	}

}
